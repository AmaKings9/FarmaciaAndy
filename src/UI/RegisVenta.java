    private void btnConfirmarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarVentaActionPerformed
        //Obtener datos necesarios de la tabla venta para el alta de venta
        DefaultTableModel prod_venta_tbl = (DefaultTableModel) tabla_venta.getModel();
        int num_rows = prod_venta_tbl.getRowCount();
        boolean flagAlta = true;
        if(num_rows > 0){
                String[][] art = new String[num_rows][3];

                for(int i=0; i<num_rows; i++){
                    System.out.println(prod_venta_tbl.getValueAt(i, 4).toString());
                    if(txtCedula.equals("") && prod_venta_tbl.getValueAt(i, 4).toString().equals("SI")){
                        JOptionPane.showMessageDialog(null, "Es necesario registrar la cédula profesional perteneciente a la receta médica");
                        flagAlta = false;
                        break;
                    } else{
                        art[i][0] = prod_venta_tbl.getValueAt(i, 0).toString();
                        art[i][1] = prod_venta_tbl.getValueAt(i, 5).toString();
                        art[i][2] = prod_venta_tbl.getValueAt(i, 2).toString();
                    }
                }
                if(flagAlta){
                    venta.setTotal_venta(Float.parseFloat(txtTotal.getText()));
                    venta.setCedula_venta(txtCedula.getText());
                    flagAlta = venta.altaVenta(empleado.getNum_empl(), art, num_rows);

                    if(flagAlta){
                        JOptionPane.showMessageDialog(null, "Registro de venta exitoso");
                    } else{
                        JOptionPane.showMessageDialog(null, "Registro fallido, intenta de nuevo");
                    }
                }
            
        } else{
            JOptionPane.showMessageDialog(null, "Debes seleccionar productos para realizar la venta");
        }
    }//GEN-LAST:event_btnConfirmarVentaActionPerformed

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        int row = tblBusqProductos.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tblBusqProductos.getModel();
        DefaultTableModel tbl_venta = (DefaultTableModel) tabla_venta.getModel();
        String M_datos[] =  new String[6];
        if(row >= 0){
            M_datos[0] = model.getValueAt(row, 0).toString();
            M_datos[1] = model.getValueAt(row, 1).toString();
            M_datos[2] = model.getValueAt(row, 2).toString();
            M_datos[3] = model.getValueAt(row, 4).toString();
            M_datos[4] = model.getValueAt(row, 5).toString();
            M_datos[5] = "1";
        }
        SetTableProdCont(M_datos);
        
        //suma del total de la venta
        String cant = JOptionPane.showInputDialog(null, "Cantidad a vender:", "Cantidad", JOptionPane.PLAIN_MESSAGE, null, null, "1").toString();
        tbl_venta.setValueAt(cant, tbl_venta.getRowCount()-1, 5);
        total = total + (Float.parseFloat(model.getValueAt(row, 2).toString()) * Float.parseFloat(cant));
        txtTotal.setText(String.valueOf(total));
    }//GEN-LAST:event_btnSeleccionarActionPerformed

