    public String[][] getTblContent(String [][] allContMed, String [][] allContProd){
    
        String[][] contFinal = new String[Integer.parseInt(allContMed[0][0]) + Integer.parseInt(allContProd[0][0])][6];
        for(int i = 1; i <= Integer.parseInt(allContMed[0][0]); i++){
            contFinal[i-1][0] = allContMed[i][0];
            contFinal[i-1][1] = allContMed[i][1];
            contFinal[i-1][2] = allContMed[i][2];
            contFinal[i-1][3] = allContMed[i][3];
            contFinal[i-1][4] = allContMed[i][4];
            contFinal[i-1][5] = allContMed[i][5];
        }
        int cont = Integer.parseInt(allContMed[0][0]);
        for(int i = 1; i <= Integer.parseInt(allContProd[0][0]); i++){
            contFinal[cont][0] = allContProd[i][0];
            contFinal[cont][1] = allContProd[i][1];
            contFinal[cont][2] = allContProd[i][2];
            contFinal[cont][3] = allContProd[i][3];
            contFinal[cont][4] = allContProd[i][4];
            contFinal[cont][5] = allContProd[i][5];
            cont++;
        }
        return contFinal;
    }
    
    public static void SetTableProdCont(String[] rowSelected){
        DefaultTableModel dtm_datos = (DefaultTableModel) tabla_venta.getModel();
        dtm_datos.addRow(rowSelected);
        tabla_venta.setModel(dtm_datos);
        TableRowSorter<TableModel> trs = new TableRowSorter<>(dtm_datos);
        tabla_venta.setRowSorter(trs);
    }
    
